package modelo.juego;

public class Atributo{
    public int id_atributo;
    public String nombre_atributo;

    public Atributo() {
    }

    public int getId_atributo() {
        return id_atributo;
    }

    public void setId_atributo(int id_atributo) {
        this.id_atributo = id_atributo;
    }

    public String getNombre_atributo() {
        return nombre_atributo;
    }

    public void setNombre_atributo(String nombre_atributo) {
        this.nombre_atributo = nombre_atributo;
    }
}

