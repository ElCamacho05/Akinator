package modelo.juego;

public class Dominio {
    public int id_dominio;
    public String nombre_dominio;

    public Dominio() {
    }

    public int getId_dominio() {
        return id_dominio;
    }

    public void setId_dominio(int id_dominio) {
        this.id_dominio = id_dominio;
    }

    public String getNombre_dominio() {
        return nombre_dominio;
    }

    public void setNombre_dominio(String nombre_dominio) {
        this.nombre_dominio = nombre_dominio;
    }
}
